def encrypt(ctxt, key):
    rk = [0] * 240
    dtxt = []
    if len(key) % 32 != 0:
        key = list(key + b'\xee' * (32 - (len(key) % 32)))
    if len(ctxt) % 16 != 0:
        ctxt = list(ctxt + b'\xee' * (16 - (len(ctxt) % 16)))

    keygen(key, rk)

    i = 0
    ctxt = list(ctxt)

    while i < len(ctxt) / 16:
        a = ctxt[i * 16:(i + 1) * 16].copy()
        ROUNDS = 14
        key_addition(a, rk[:16])

        r = 1
        while r < ROUNDS:
            s_box(a, S)
            shift_row(a, 0)
            mix_column(a)
            key_addition(a, rk[r * 16:(r + 1) * 16])

            r += 1

        s_box(a, S)
        shift_row(a, 0)
        key_addition(a, rk[16 * ROUNDS: (ROUNDS + 1) * 16])

        dtxt.extend(a)
        i = i + 1

    return bytes(dtxt)


def decrypt(ctxt, key):
    rk = [0] * 240
    dtxt = []
    if len(key) % 32:
        key = list(key + b'\xee' * (32 - (len(key) % 32)))

    keygen(key, rk)

    ctxt = list(ctxt)
    i = 0
    while i < len(ctxt) / 16:
        ROUNDS = 14
        a = ctxt[i * 16:(i + 1) * 16].copy()
        key_addition(a, rk[ROUNDS * 16:(ROUNDS + 1) * 16])
        s_box(a, Si)
        shift_row(a, 1)

        r = ROUNDS - 1
        while r > 0:
            key_addition(a, rk[r * 16:(r + 1) * 16])
            inv_mix_column(a)
            s_box(a, Si)
            shift_row(a, 1)
            r -= 1

        key_addition(a, rk)

        dtxt.extend(a)
        i += 1

    return bytes(dtxt)
